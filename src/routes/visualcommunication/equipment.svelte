<h2>Electrical Cutter</h2>
<p>
	This cutter is perfect for all kinds of media cutting applications including
	vinyl, heat and image transfer media, card stock, contour cut stickers,
	labels, wrappers, hang tags, stencil cutting for paint masking and etching
	masking, plus many others applications.
</p>
<h2>Manual Guillotine Paper Cutter</h2>
<p>
	This heavy duty stack cutter is capable of easily slicing through 200 sheets
	of 20lb paper up to 17″ long. It’s equipped with precision measurement guides,
	a hand driven paper stop, a wheel is driven paper clamp to hold your paper in
	place, and some safety features,
</p>
<h2>Rotary Paper Trimmer</h2>
<p>
	Rotary paper trimmers use a round cutting blade, which slides along a rail.
	Rotary trimmers provide a very clean, accurate cut. Rotary trimmers work well
	for paper, photographs, and other materials.
</p>
<h2>Multi-Purpose Press</h2>
<p>
	This is a multipurpose press that can be used for: bookmaking, printmaking,
	pressed plants, collage, stamps, tearing straight/deckled edges. The robust
	construction and smooth turning spindle apply great pressure easily by hand.
	The aluminum press plate ensures uniform pressure over the entire surface.
	Working dimension: 15 1/4” x 10 1/2” x 5” high.
</p>
<h2>Saddleback Stapler</h2>
<p>
	This stapler is designed to insert staples into the spine (saddle) of folded
	printed matter such as booklets, catalogs, brochures, and manuals. They have
	an unusual length and by the presence of a V-shaped hump which is used to
	precisely align the central fold of the material to be stapled.
</p>
